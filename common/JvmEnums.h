enum CLASS_ACCESS_FLAGS_ENUM {
    ACC_PUBLIC = 0x0001,  //声明为public,可以被包外访问
    ACC_FINAL = 0x0010,   //声明为final,不允许有子类
    ACC_SUPER = 0x0020,    //当用到invokespecial指令时，需要特殊处理
    ACC_INTERFACE = 0x0200,
    ACC_ABSTRACT = 0x0400,
    ACC_SYNTHETIC = 0x1000,
    ACC_ANNOTATION = 0x2000,
    ACC_ENUM = 0x4000
};

